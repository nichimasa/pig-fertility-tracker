import pandas as pd

# CSVファイルを読み込む
df = pd.read_csv('data/種付記録一覧_20251123150908.csv', encoding='utf-8-sig')

# ===================
# 妊娠鑑定結果を確認
# ===================
print("=== 妊娠鑑定結果の種類 ===")
print(df['妊娠鑑定結果'].value_counts())

# ===================
# 産次の分布を確認
# ===================
print("\n=== 産次の分布 ===")
print(df['産次'].value_counts().sort_index())

# ===================
# 使用した精液の種類を確認
# ===================
print("\n=== 精液の種類 ===")
print(df['雄豚・精液・あて雄'].value_counts())

# ===================
# 種付日の範囲を確認
# ===================
print("\n=== 種付日の範囲 ===")
print(f"最初の種付日: {df['種付日'].min()}")
print(f"最後の種付日: {df['種付日'].max()}")